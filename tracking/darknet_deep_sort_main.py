def split_darknet_detections(detections):
    bboxs=[]
    confidences=[]
    labels=[]
    for label, confidence, bbox in detections:
        #bboxs.append(bbox)  # this bbox's (x,y) is the center point
        _box=[]   # this _box's (x,y) is the top-left point
        x = int(bbox[0]-bbox[2]/2)
        _box.append(x)
        y = int(bbox[1]-bbox[3]/2)
        _box.append(y)
        _box.append(bbox[2])
        _box.append(bbox[3])
        bboxs.append(_box)
        confidences.append(confidence)
        labels.append(label)
    return labels,confidences,bboxs
			
def detect_tracking(frame_queue, detections_queue, fps_queue):
    # generate different colors for different classes  
    classesNum = len(class_names)
    # ----------------------------------
    # tracking with deep_sort
    # ----------------------------------
    # Definition of the parameters
    tracking = True
    max_cosine_distance = 0.5
    nn_budget = None
    nms_max_overlap = 1.0
 
    # Deep SORT
    model_filename = 'yolov3_deepsort/model_data/mars-small128.pb'
    encoder = gdet.create_box_encoder(model_filename, batch_size=1)
    metric = nn_matching.NearestNeighborDistanceMetric("cosine", max_cosine_distance, nn_budget)
    tracker = Tracker(metric,max_age=100)

    random.seed(3)  # deterministic bbox colors
    video = set_saved_video(cap, args.out_filename, (width, height))
    while cap.isOpened():
        frame_resized = frame_queue.get()
        detections = detections_queue.get()
        fps = fps_queue.get()
        if frame_resized is not None:
            image = darknet.draw_boxes(detections, frame_resized, class_colors)
            image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
            labels, confidences, boxes = split_darknet_detections(detections)
            names = labels
            if tracking:
                features = encoder(image, boxes)
                detections = [Detection(bbox, score, class_name, feature) for bbox, score, class_name, feature in zip(boxes, confidences, names, features)]
            if tracking:
                tracker.predict()
                tracker.update(detections)
                for track in tracker.tracks:
                    if not track.is_confirmed() or track.time_since_update > 1:
                        continue
                    bbox = track.to_tlbr()
                    class_name = track.get_class()
                    color = class_colors[class_name]
                    #cv2.rectangle(image, (int(bbox[0]), int(bbox[1])), (int(bbox[2]), int(bbox[3])), color, 2)
                    cv2.rectangle(image, (int(bbox[0]), int(bbox[1]-30)), (int(bbox[0])+(len(class_name)+len(str(track.track_id)))*17, int(bbox[1])), color, -1)
                    cv2.putText(image, class_name + "-" + str(track.track_id),(int(bbox[0]), int(bbox[1]-10)),0, 0.75, (255,255,255),2)
            
            if args.out_filename is not None:
                video.write(image)
            if not args.dont_show:
                cv2.imshow('Tracking', image)
            if cv2.waitKey(fps) == 27:
                break
    cap.release()
    video.release()
    cv2.destroyAllWindows()

